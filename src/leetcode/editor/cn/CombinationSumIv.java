    // 1 1 2 4 7  j=4   => dp[target]
    public int combinationSum4(int[] nums, int target) {
        int[] dp = new int[target+1];
        // åˆå§‹åŒ–
        dp[0]=1;
        // éå†ï¼Œå…ˆé‡é‡ï¼Œå†ç‰©å“
        for (int j = 1; j <= target; j++) {
            for (int i = 0; i < nums.length; i++) {
                if (j>=nums[i]){
                    dp[j]+=dp[j-nums[i]];
                }
            }
        }
        return dp[target];
    }
